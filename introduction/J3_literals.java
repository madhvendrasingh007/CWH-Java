package introduction;

public class J3_literals {
	public static void main(String[] args) {
		int age = 18;	//integer literal
		System.out.println(age);
		
		char ch = 'b';
		System.out.println(ch);
		
		long l = 1200000;
		System.out.println(l);
		
		float a = 12.2f;	//f is written as its default is double
		System.out.println(a);
		
		double d1 = 122.2; //no need to write d by default its d;
		System.out.println(d1);
		
		boolean b = true;
		System.out.println(b);
		
		String str = "hello world";
		System.out.println(str);
	}
}

//Literal: Any constant value which can be assigned to the variable is called literal/constant. 